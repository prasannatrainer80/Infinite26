-- Aggregate Functions 

-- Sum() : Used to perform sum operation on the given numeric field

select sum(sal) from Emp;

-- avg() : Used to perform avg operation on the given numeric field

select avg(sal) from Emp;

-- max() : used to display max value of numeric field specified

select max(sal) from Emp;

-- min() : Used to display min value of numeric field specified

select min(sal) from Emp;

-- count(*) : Used to display total records of the table 

select count(*) from Emp;